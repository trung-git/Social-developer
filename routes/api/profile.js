// @route     DELETE api/profiles/experience/:id
// @desc      Remove profile experience
// @access    Private
router.delete("/experience/:expId", auth, async (req, res) => {
  try {
    const profile = await Profile.findOne({ user: req.user.id });

    // Get remove index
    const expIndex = profile.experience.map((item) => {
      return item.id;
    });
    let removeIndex = null;
    expIndex.forEach((i) => {
      if (i === req.params.expId) {
        removeIndex = expIndex.indexOf(req.params.expId);
      }
    });

    if (removeIndex !== null) profile.experience.splice(removeIndex, 1);

    await profile.save();
    res.json(profile);
  } catch (err) {
    console.error(err.message);
    res.status(500).send("Server error");
  }
});

